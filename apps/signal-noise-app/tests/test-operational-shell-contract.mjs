test('operational strip dedupes combined backlog diagnostics items before rendering keyed cards', async () => {
  const stripSource = await readFile(new URL('../src/components/layout/OperationalStatusStrip.tsx', import.meta.url), 'utf8')

  assert.match(stripSource, /dedupeSnapshotLaneItems/)
  assert.match(stripSource, /items=\{dedupeSnapshotLaneItems\(\[\.\.\.staleActiveRows, \.\.\.blockedEntities, \.\.\.resumedEntities\]\)\}/)
  assert.doesNotMatch(stripSource, /key=\{item\.entity_id \|\| `\$\{title\}-\$\{index\}`\}/)
})
