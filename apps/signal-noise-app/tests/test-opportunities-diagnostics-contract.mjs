test('opportunities diagnostics verify-now lane excludes sparse failed and context-only rows', () => {
  const source = readFileSync(routePath, 'utf8')

  assert.match(source, /recommendation_tier:\s*'verify_now'/)
  assert.match(source, /coalesce\(i\.answer_count,\s*0\)\s*>=\s*5/)
  assert.match(source, /coalesce\(i\.evidence_count,\s*0\)\s*>=\s*3/)
  assert.match(source, /coalesce\(m\.yellow_panther_fit,\s*0\)\s*>=\s*60/)
  assert.match(source, /<> 'context_only'/)
  assert.match(source, /<> 'failed'/)
  assert.match(source, /coalesce\(m\.raw_payload->>'shortlist_opportunity',\s*'false'\)\s*<> 'true'/)
  assert.match(source, /suggestedVerificationActionFor/)
})

